// to print no. from 1 to 10.
class Main {
    public static void main(String[] args) {
        
        for (int i=1;i<=10;i++){
            System.out.println(i);
        }
    }
}

// to print no. from 10 to 1.
class Main {
    public static void main(String[] args) {
       
        for (int i=10;i>=1;i--){
            System.out.println(i);
        }
    }
}
